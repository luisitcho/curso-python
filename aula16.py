entrada = input('Você quer "entrar" ou "sair"? ')

if entrada == 'entrar':
    print('Você entrou no sistema')
    print(1)
    print(2)
    print(3)
elif entrada == 'sair':
    print('Você saiu do sistema')
    print(4)
    print(5)
    print(6)
else:
    print('Você não entrou nem saiu do sistema')
    print(7)
    print(8)
    print(9)

print('Fim do if')